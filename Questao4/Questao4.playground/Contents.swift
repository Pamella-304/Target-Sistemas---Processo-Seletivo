//a) -> 1, 3, 5, 7, 9. É uma simples sequência de números ímpares e, por isso, o último elemento é 9.

//b) -> 2, 4, 8, 16, 32, 64, 128. Cada número da sequência corresponde ao dobro do número anterior. Por isso, o último elemento é 128.

//c) -> 0, 1, 4, 9, 16, 25, 36, 49. Os números da sequencia representam os quadrados perfeitos dos números inteiros. Por isso, o último elemento é 49, que é 7 elevado ao quadrado.

//d) -> 4, 16, 36, 64, 100. Essa sequencia corresponde aos quadrados perfeitos dos números pares. Por isso, o ultimo número é 100, que é 10 elevado ao quadrado.

//e) 1, 1, 2, 3, 5, 8, 13. Essa é a sequência de Fibonacci. Por isso, o último elemento é o 13.

//f) 2,10, 12, 16, 17, 18, 19, 20. Aqui, a lógica consiste em alternar entre adicionar 8 e adicionar 1 a números consecutivos.
